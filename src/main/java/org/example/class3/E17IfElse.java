package org.example.class3;

public class E17IfElse {
    public static void main(String[] args) {

        boolean isRaining=false;

        if(isRaining){
            // if(true or false) it will print to whatever is true
            System.out.println("Take the umbrella");
            System.out.println("Let's take our jacket too");
        }
        System.out.println("Let's go to the beach");

    }

}
